#include<iostream>

using namespace std;

int main(int argc, char** argv)
{
	int test_case;
	int T;
	cin>>T;

	for(test_case = 1; test_case <= T; ++test_case)
	{
        
        int A, B, N;
        cin >> A >> B >> N;

        int cnt = 0;
        while (1) {
            /*
            1 2
            3 2
            5 3
            */
            if (A > B) {
                B += A;
            }
            else {
                A += B;
            }
            cnt++;

            if (A > N || B > N) {
                break;
            }
        }

        cout << cnt << endl;

	}
	return 0;//정상종료시 반드시 0을 리턴해야합니다.
}