import java.util.Scanner;
public class Main10951 {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);

        while (sc.hasNext()) { //hasNext() 란 무엇인가?
            int a  = sc.nextInt();
            int b = sc.nextInt();

            System.out.println(a + b);
        }
    }
}
